package Java_Notes;

public class NamingConventions {
    /*
     * Java has a couple of standard naming conventions:
     * 
     * 1. All classes should utilize PascalCase syntax
     *  -All unique words in the class name should start with a capital letter
     *  - no classes should start with a number for their name.
     * 
     * 2. All variables (references to other data) should be written in camelCase syntax
     *  - first unique word is all lowercase, every unique work after starts with capital letter
     *      -example: varOne, myCat, thisIsAReallyLongVariableName
     * 3. Any constant values should be written in SCREAMING_SNACK_CASE(__underline!)
     */
}
